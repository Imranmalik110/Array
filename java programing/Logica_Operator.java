import java.lang.String;
import java.util.Scanner;
class Logica_Operator
{
    public static void main(String[] args) {
        boolean b1=true;
        boolean b2=false;
        System.out.println(" If (b1 && b2) "+(b1 && b2));
        System.out.println("If (b2 || b2) "+(b1 || b2));
        System.out.println("If !(b1 && b2) "+!(b1 && b2));
    }
}